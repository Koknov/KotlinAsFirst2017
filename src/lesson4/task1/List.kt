/**
 * Средняя
 *
 * Перевести заданное целое число n >= 0 в систему счисления с основанием base > 1.
 * Результат перевода вернуть в виде списка цифр в base-ичной системе от старшей к младшей,
 * например: n = 100, base = 4 -> (1, 2, 1, 0) или n = 250, base = 14 -> (1, 3, 12)
 */
fun convert(n: Int, base: Int): List<Int> {
    var num2 = n
    val a = mutableListOf<Int>()
    do {
        a.add(num2 % base)
        num2 /= base
    } while (num2 > 0)
    return a.asReversed()
}

/**
 * Сложная
 *
 * Перевести заданное целое число n >= 0 в систему счисления с основанием 1 < base < 37.
 * Результат перевода вернуть в виде строки, цифры более 9 представлять латинскими
 * строчными буквами: 10 -> a, 11 -> b, 12 -> c и так далее.
 * Например: n = 100, base = 4 -> 1210, n = 250, base = 14 -> 13c
 */
fun convertToString(n: Int, base: Int): String {
    val a = convert(n, base)
    val b = mutableListOf<String>()
    for (char in a)
        if (char > 9)
            b.add(('W' + char).toString())
        else b.add(char.toString())
    return b.joinToString(separator = "")
}




/**
 * Средняя
 *
 * Перевести число, представленное списком цифр digits от старшей к младшей,
 * из системы счисления с основанием base в десятичную.
 * Например: digits = (1, 3, 12), base = 14 -> 250
 */
fun decimal(digits: List<Int>, base: Int): Int {
    var num = 0.0
    val a = digits.reversed()
    for (i in 0 until a.size)
        num += a[i] * pow(base.toDouble(), i.toDouble())
    return num.toInt()
}

/**
 * Сложная
 *
 * Перевести число, представленное цифровой строкой str,
 * из системы счисления с основанием base в десятичную.
 * Цифры более 9 представляются латинскими строчными буквами:
 * 10 -> a, 11 -> b, 12 -> c и так далее.
 * Например: str = "13c", base = 14 -> 250
 */
fun decimalFromString(str: String, base: Int): Int {
    val a = mutableListOf<Int>()
    for (char in str)
        if (char in 'a'..'z')
            a.add(char - 'a' + 10)
        else a.add(char - '0')
    return decimal(a, base)
}

/**
 * Сложная
 *
 * Перевести натуральное число n > 0 в римскую систему.
 * Римские цифры: 1 = I, 4 = IV, 5 = V, 9 = IX, 10 = X, 40 = XL, 50 = L,
 * 90 = XC, 100 = C, 400 = CD, 500 = D, 900 = CM, 1000 = M.
 * Например: 23 = XXIII, 44 = XLIV, 100 = C
 */
fun roman(n: Int): String {
    val roman = listOf("M", "CM", "D", "CD", "C", "XC", "L", "XL", "X", "IX", "V", "IV", "I")
    val digits = listOf(1000,900,500,400,100,90,50,40,10,9,5,4,1)
    var result = ""
    var a = 0
    var num = n
    while (num > 0) {
        if (num >= digits[a]) {
            result += roman[a]
            num -= digits[a]
        }
        else
            a++
    }
    return result
}
